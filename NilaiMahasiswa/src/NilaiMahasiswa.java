    private void tableData() {
        try {
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            
            while (resultSet.next()) {
                String nim = resultSet.getString("nim");
                String nama = resultSet.getString("nama");
                int tugas = resultSet.getInt("tugas");
                int kuis = resultSet.getInt("kuis");
                int uts = resultSet.getInt("uts");
                int uas = resultSet.getInt("uas");
                float rerata = (tugas + kuis + uts + uas) / 4;
                DecimalFormat dcFormat = new DecimalFormat("#.00");
                
                String grade = getGrade(rerata);
                String ket = (rerata >= 70) ? "Lulus" : "Tidak Lulus";
                
                model.addRow(new String[]{
                    nim, 
                    nama, 
                    Integer.toString(tugas), 
                    Integer.toString(kuis), 
                    Integer.toString(uts), 
                    Integer.toString(uas),
                    dcFormat.format(rerata),
                    grade,
                    ket
                });
            }
            
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
