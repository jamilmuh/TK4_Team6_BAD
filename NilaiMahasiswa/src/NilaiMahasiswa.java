    private void simpanbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanbtnActionPerformed
        try {
            String nimval = nim.getText();
            String namaval = nama.getText();
            int tugasval = Integer.parseInt(tugas.getText());
            int kuisval = Integer.parseInt(kuis.getText());
            int utsval = Integer.parseInt(uts.getText());
            int uasval = Integer.parseInt(uas.getText());

            if (formTitle.getText().contains("Tambah")) {
                String sql = "INSERT INTO nilai (nim, nama, tugas, kuis, uts, uas) VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement preparedStatement = connection.prepareStatement(sql);
                preparedStatement.setString(1, nimval);
                preparedStatement.setString(2, namaval);
                preparedStatement.setInt(3, tugasval);
                preparedStatement.setInt(4, kuisval);
                preparedStatement.setInt(5, utsval);
                preparedStatement.setInt(6, uasval);
                preparedStatement.executeUpdate();
                
                showMessageDialog("Success", "Nilai berhasil ditambahkan!");
                
            } else if (formTitle.getText().contains("Edit")) {
                String sql = "UPDATE nilai SET nim=?, nama=?, tugas=?, kuis=?, uts=?, uas=? WHERE id=?";
                PreparedStatement preparedStatement = connection.prepareStatement(sql);
                preparedStatement.setString(1, nimval);
                preparedStatement.setString(2, namaval);
                preparedStatement.setInt(3, tugasval);
                preparedStatement.setInt(4, kuisval);
                preparedStatement.setInt(5, utsval);
                preparedStatement.setInt(6, uasval);
                preparedStatement.setInt(7, editId);
                preparedStatement.executeUpdate();
                
                showMessageDialog("Success", "Nilai berhasil diperbarui!");
            }
            
            
            jDialog1.setVisible(false);
            loadData();
            tableData();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_simpanbtnActionPerformed

