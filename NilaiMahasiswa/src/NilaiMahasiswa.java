    public static String getGrade(double rerata) {
        if (rerata >= 90 && rerata <= 100) {
            return "A";
        } else if (rerata >= 85) {
            return "A-";
        } else if (rerata >= 80) {
            return "B+";
        } else if (rerata >= 75) {
            return "B";
        } else if (rerata >= 70) {
            return "B-";
        } else if (rerata >= 65) {
            return "C+";
        } else if (rerata >= 60) {
            return "C";
        } else if (rerata >= 55) {
            return "C-";
        } else if (rerata >= 50) {
            return "D+";
        } else if (rerata >= 45) {
            return "D";
        } else if (rerata < 45 && rerata >= 0) {
            return "E";
        } else {
            return "Nilai tidak valid";
        }
    }
    
