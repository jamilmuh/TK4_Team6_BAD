    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        int selected = jTable1.getSelectedRow();
        
        try {
            String sql = "SELECT id FROM nilai WHERE nim=?";
            PreparedStatement preparedStatement = connection.prepareStatement(sql);
            preparedStatement.setString(1, jTable1.getValueAt(selected, 0).toString());
            ResultSet result = preparedStatement.executeQuery();
            result.next();
            int id = result.getInt("id");
            editId = id;
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        nim.setText(jTable1.getValueAt(selected, 0).toString());
        nama.setText(jTable1.getValueAt(selected, 1).toString());
        tugas.setText(jTable1.getValueAt(selected, 2).toString());
        kuis.setText(jTable1.getValueAt(selected, 3).toString());
        uts.setText(jTable1.getValueAt(selected, 4).toString());
        uas.setText(jTable1.getValueAt(selected, 5).toString());
        reratatext.setText(jTable1.getValueAt(selected, 6).toString());
        gradetext.setText(jTable1.getValueAt(selected, 7).toString());
        kettext.setText("Dinyatakan " + jTable1.getValueAt(selected, 8).toString());
        
        formTitle.setText("Edit Data");
        jDialog1.pack();
        jDialog1.setVisible(true);
    }//GEN-LAST:event_editbtnActionPerformed

