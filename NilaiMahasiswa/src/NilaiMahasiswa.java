    private void hitungbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungbtnActionPerformed
        int tugasval = Integer.parseInt(tugas.getText());
        int kuisval = Integer.parseInt(kuis.getText());
        int utsval = Integer.parseInt(uts.getText());
        int uasval = Integer.parseInt(uas.getText());
        
        float rerata = (float) (tugasval + kuisval + utsval + uasval) / 4;
        DecimalFormat dcFormat = new DecimalFormat("#.00");
        String grade = getGrade(rerata);
        String ket = (rerata >= 70) ? "Lulus" : "Tidak Lulus";
        
        reratatext.setText(dcFormat.format(rerata));
        gradetext.setText(grade);
        kettext.setText("Dinyatakan " + ket);
    }//GEN-LAST:event_hitungbtnActionPerformed

